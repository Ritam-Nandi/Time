    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int day=Integer.parseInt(jTextField5.getText().toString());
        int hour=Integer.parseInt(jTextField6.getText().toString());
        int minute=Integer.parseInt(jTextField7.getText().toString());
        Date d= absolute;
        
        Calendar current = Calendar.getInstance();
        current.setTime(d);
        current.add(Calendar.DATE,-day);
        current.add(Calendar.HOUR_OF_DAY, -hour);
        current.add(Calendar.MINUTE, -minute);
        setStuffs(current.getTime());
        absolute=current.getTime();
    }//GEN-LAST:event_jButton3ActionPerformed

