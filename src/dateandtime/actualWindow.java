    private void setStuffs(Date d){
        Date ist = d, gmt = d,pst = d,est = d;
        
        Calendar cGMT =Calendar.getInstance();
        cGMT.setTime(gmt);
        cGMT.add(Calendar.HOUR_OF_DAY, -5);
        cGMT.add(Calendar.MINUTE, -30);
        //cGMT.setTime(gmt);
        
        Calendar cPST =Calendar.getInstance();
        cPST.setTime(pst);
        cPST.add(Calendar.HOUR_OF_DAY, -13);
        cPST.add(Calendar.MINUTE, -30);
        //cPST.setTime(pst);
        
        Calendar cEST =Calendar.getInstance();
        cEST.setTime(est);
        cEST.add(Calendar.HOUR_OF_DAY, -10);
        cEST.add(Calendar.MINUTE, -30);
        //cEST.setTime(est);
        
        
        jLabel5.setText(ist.toString().replace("IST", ""));
        jLabel6.setText(cGMT.getTime().toString().replace("IST", ""));
        jLabel7.setText(cPST.getTime().toString().replace("IST", ""));
        jLabel8.setText(cEST.getTime().toString().replace("IST", ""));
        
    }
    
